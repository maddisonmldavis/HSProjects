public class Triangle
{
	// instance variables 
	private int length;
	private int width;

	/**
	 * Constructor for objects of class triangle
	 */
	public Triangle(int l, int w)
	{
		// initialise instance variables
		length = l;
		width = w;
	}

	// return the height
	public int getLength()
	{
		return length;
	}
	public int getWidth()
	{
	    return width;
	}
	   
}
